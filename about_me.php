<br><br><br>
	<img src="res/img_about/imagen1.jpeg" alt="" style="width: 100%"><br><br><br>
	<p>
		El Coro Acción Comunitaria(Coral Comunity) nació como el coro oficial de la parroquia San Rafael Arcángel de la comunidad 24 de abril el dia 29 de septiembre de 2010, fue fundado por el profesor Luis A. De la Cruz Martínez (Richard).Para ese entonces su propósito era la exaltación y adoración a Dios y llevar a la comunidad el mensaje de evangelización a través de sus canciones, teniendo como apoyo al reverendo padre Joselito Beltré.
	</p>
	<p>
		El coro ha cantado en varias parroquias de la provincia de Santo Domingo y el Distrito Nacional incluyendo La Catedral Primada de América, en el 2011 participó en el "Festival de Los Coros" realizado en el Parque Colón en conmemoración del Jubileo de los 500 años de la evangelización de Santo Domingo.El 1ro. de octubre de 2011 celebró su primer aniversario con un concierto titulado "Este Es El Día" donde fue invitado el coro Voces de San Martin de la Parroquia San Ignacio de Loyola del sector La Ciénaga.
	</p>
	<p>
		En diciembre de ese mismo año llevó su voz a la Base Aérea de San Isidro cantando villancicos en el Club de Esposas de Oficiales.Fue invitado por el Director del Coro Nacional Prof. Simón Cesar Nova para cantar en la misa de celebración del 57 aniversario de la fundación de dicho coro.
	</p>
	<p>
		Participó en la 5ta. Versión del Reality Show "Mi Voz Para Cristo" llegando a la Gran Final celebrada el 22 de abril de 2012 en la Sala Principal del Teatro Nacional Eduardo Brito.
	</p>
	<p>
		El 29 de septiembre de 2012 realizó un encuentro de coros para festejar el 2do. aniversario titulado "Dad Gracias a Dios" en donde participaron los coros: Maximiliano Kolbe, Luz Vida y COPACREY.
	</p>
	<p>
		Desde el 5 de Julio del año 2015 el coro Voces de San Rafael pasó a ser no solamente parte de la Parroquia sino a ser el coro de la junta de Vecinos del sector 24 de Abril llamada: Acción Comunitaria, abarcando así los sectores aledaños: Simón Bolívar, Ensanche Espaillat, y las cañitas; con su nuevo nombre "Coro Acción Comunitaria" adquirió el lema "Más Que Un Coro, Una Hermandad".
	</p>
	<p>
		El Coro Acción Comunitaria es parte de los trabajos sociales que realiza la Junta de Vecinos del Sector 24 de abril para fomentar la cultura en dicha comunidad la cual está presidida por la Lcda. Crisaurys De la Cruz y el Señor Andrés Pérez.
	</p>
	<p>
		Surgió en el corazón de Richard expandir el coro para resaltar y proyectar los valores artísticos - culturales de la juventud de dichos sectores y así mantenerlos en la sana e importante disciplina del canto coral.
	</p>
	<p>
		El coro ya contando con una membresía de 30 integrantes se presentó por primera vez en octubre del año 2015 en el festival de coros que realiza el Instituto Cultural Dominico Americano en el auditórium Patrick N. Houghton, luego en la Asamblea General Ordinaria de la Cooperativa del Grupo Malla efectuada en el Salón La Mancha del Hotel Barceló antiguo Lina.
	</p>
	<p>
		En diciembre de ese mismo año realizó un concierto Navideño en la Parroquia San Rafael Arcángel del sector 24 de abril.
	</p>
	<p>
		Los días 18 y 19 de junio de 2016 realizó dos conciertos en el municipio de Río San Juan de la provincia María Trinidad Sánchez en conmemoración de sus fiestas patronales presentándose allá en varias parroquias y en hogares de destacadas personalidades como lo es el compositor Juan Eddy Paulino; ese mismo año en Sánchez Samaná fue parte de la celebración de la acostumbrada actividad "Sancheros Ausentes", en donde ofreció un concierto.
	</p>
